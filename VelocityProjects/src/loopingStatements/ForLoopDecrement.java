public class ForLoopDecrement {

public static void main(String[] args) {
		
		// Steps ------>>> Initialization, Condition, Statement Execution, Decrement
		
		
		for(int i= 5 ; i >=0 ; i--)						
																	 
		{
			System.out.println("Number is = " + i);

		}
		

	}

}

