package conversionPrimitive;

public class Primitive_ImplicitCasting {

	public static void main(String[] args) {
		
		short s = 100;			// memory of short is 2 bytes
		int i = s;				// memory of int is 4 bytes
		System.out.println(i);
		
		
		int a = 500;			// memory of int is 4 bytes
		long l = a;				// memory of long is 8 bytes
		System.out.println(l);
		
	}

}
